  function estvLookup(comm) {
    const norm = comm.replace(/\./g,'').replace(/-/g,' ').replace(/\s+/g,' ').trim().toLowerCase();
    if (ESTV[norm]) return ESTV[norm];
    // Try without suffix (zh), (be) etc.
    const short = norm.replace(/ \(.*\)$/, '').trim();
    if (ESTV[short]) return ESTV[short];
    // Fuzzy: first word match
    const first = norm.split(' ')[0];
    if (first.length > 4) {
      const match = Object.entries(ESTV).find(([k]) => k.startsWith(first));
      if (match) return match[1];
    }
    return null;
  }

  function estvNationalPct(total) {
    const n = ESTV_SORTED.length;
    return Math.round(ESTV_SORTED.filter(x => x <= total).length / n * 100);
  }

